package com.empmngt.enities;

public class leavesStock {
	
	private int eid;
	private int consumedLeaves;
	private int availableLeaves;
	public int getEid() {
		return eid;
	}
	public void setEid(int eid) {
		this.eid = eid;
	}
	public int getConsumedLeaves() {
		return consumedLeaves;
	}
	public void setConsumedLeaves(int consumedLeaves) {
		this.consumedLeaves = consumedLeaves;
	}
	public int getAvailableLeaves() {
		return availableLeaves;
	}
	public void setAvailableLeaves(int availableLeaves) {
		this.availableLeaves = availableLeaves;
	}
	
	

}
